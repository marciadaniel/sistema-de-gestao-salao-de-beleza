public void readTableFornecedores(){
       DefaultTableModel modelo= (DefaultTableModel) tableFornecedores.getModel();
       modelo.setNumRows(0);
       tableFornecedores.getColumnModel().getColumn(0).setPreferredWidth(50);
       tableFornecedores.getColumnModel().getColumn(1).setPreferredWidth(250);
       tableFornecedores.getColumnModel().getColumn(2).setPreferredWidth(250);
       tableFornecedores.getColumnModel().getColumn(3).setPreferredWidth(250);
       tableFornecedores.getColumnModel().getColumn(4).setPreferredWidth(300);
       
       FornecedorDAO dao=new FornecedorDAO();
       
       for(Fornecedor c: dao.Read()){
       
       modelo.addRow(new Object[]{
       c.getIdFornecedor(),
       c.getNome(),
       c.getEndereco(),
       c.getTelefone(),
       c.getEmail(),
       });
       
       
       }
    
       }
