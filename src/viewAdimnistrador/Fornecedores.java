    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
       if (tableFornecedores.getSelectedRow() !=-1)
    {
     Fornecedor c=new Fornecedor();
        FornecedorDAO dao=new FornecedorDAO();
        
        c.setNome(txtNome.getText());
        int intTelefone=Integer.parseInt(txtTelefone.getText());
        c.setTelefone(intTelefone);
        c.setEndereco(txtEndereco.getText());
        c.setEmail(txtEmail.getText());
       
        c.setIdFornecedor((int)tableFornecedores.getValueAt(tableFornecedores.getSelectedRow(),0));
        dao.alterar(c);
        txtNome.setText("");
        txtEndereco.setText("");
        txtTelefone.setText("");
        txtEmail.setText("");
        readTableFornecedores();
    
    }
       else
        {
        JOptionPane.showMessageDialog(null, "Selecione a linha a ser alterada!", "erro", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

