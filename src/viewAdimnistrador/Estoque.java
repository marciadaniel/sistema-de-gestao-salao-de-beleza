    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (tableEstoque.getSelectedRow() !=-1)
    {
     EstoqueModel c=new EstoqueModel();
        EstoqueDAO dao=new EstoqueDAO();
        c.setItemdeestoque(txtItem.getText());
        int intQtd=Integer.parseInt(txtQtd.getText());
        c.setQuantidade(intQtd);
         int intPreco=Integer.parseInt(txtPreco.getText());
        c.setPreco(intPreco);
        int intFornecedor=Integer.parseInt(txtFornecedor.getText());
        c.setFornecedor(intFornecedor);
        
       
        c.setCodigo((int)tableEstoque.getValueAt(tableEstoque.getSelectedRow(),0));
        dao.alterar(c);
        
        readTableEstoque();
    txtItem.setText("");
        txtQtd.setText("");
        txtPreco.setText("");
        txtFornecedor.setText("");
    }
       else
        {
        JOptionPane.showMessageDialog(null, "Selecione a linha a ser alterada!", "erro", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

