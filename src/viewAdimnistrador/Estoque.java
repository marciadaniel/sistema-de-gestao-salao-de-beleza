    public void readTableEstoque(){
       DefaultTableModel modelo= (DefaultTableModel) tableEstoque.getModel();
       modelo.setNumRows(0);
       tableEstoque.getColumnModel().getColumn(0).setPreferredWidth(50);
       tableEstoque.getColumnModel().getColumn(1).setPreferredWidth(250);
       tableEstoque.getColumnModel().getColumn(2).setPreferredWidth(250);
       tableEstoque.getColumnModel().getColumn(3).setPreferredWidth(250);
       tableEstoque.getColumnModel().getColumn(4).setPreferredWidth(300);
      
       EstoqueDAO dao=new EstoqueDAO();
       
       for(EstoqueModel c: dao.Read()){
       
       modelo.addRow(new Object[]{
       c.getCodigo(),
       c.getItemdeestoque(),
       c.getPreco(),
       c.getQuantidade(),
       c.getFornecedor()
       
       });
       
       
       }
    
       }
