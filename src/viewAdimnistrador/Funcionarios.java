    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (tableFuncionarios.getSelectedRow() !=-1)
    {
     Funcionario c=new Funcionario();
        FuncionarioDAO dao=new FuncionarioDAO();
        
        c.setNome(txtNome.getText());
        int intTelefone=Integer.parseInt(txtTelefone.getText());
        c.setTelefone(intTelefone);
        c.setMorada(txtMorada.getText());
        c.setFuncao(txtFuncao.getText());
        c.setEmail(txtEmail.getText());
        c.setSexo((String) cmbSexo.getSelectedItem());
        c.setIdFuncionario((int)tableFuncionarios.getValueAt(tableFuncionarios.getSelectedRow(),0));
        dao.alterar(c);
        txtNome.setText("");
        txtMorada.setText("");
        txtTelefone.setText("");
        txtEmail.setText("");
        readTableFuncionarios();
    
    }else
        {
        JOptionPane.showMessageDialog(null, "Selecione a linha a ser alterada!", "erro", JOptionPane.WARNING_MESSAGE);
        } 
            
       
            
    }//GEN-LAST:event_btnAlterarActionPerformed

