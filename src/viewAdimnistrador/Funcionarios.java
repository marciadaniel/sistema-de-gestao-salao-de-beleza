   public void readTableFuncionarios(){
    DefaultTableModel modelo= (DefaultTableModel) tableFuncionarios.getModel();
       modelo.setNumRows(0);
       tableFuncionarios.getColumnModel().getColumn(0).setPreferredWidth(50);
       tableFuncionarios.getColumnModel().getColumn(1).setPreferredWidth(250);
       tableFuncionarios.getColumnModel().getColumn(2).setPreferredWidth(250);
       tableFuncionarios.getColumnModel().getColumn(3).setPreferredWidth(250);
       tableFuncionarios.getColumnModel().getColumn(4).setPreferredWidth(300);
       tableFuncionarios.getColumnModel().getColumn(5).setPreferredWidth(80);
       tableFuncionarios.getColumnModel().getColumn(6).setPreferredWidth(300);
       FuncionarioDAO dao=new FuncionarioDAO();
       
       for(Funcionario c: dao.Read()){
       
       modelo.addRow(new Object[]{
       c.getIdFuncionario(),
       c.getNome(),
       c.getMorada(),
       c.getTelefone(),
       c.getEmail(),
       c.getSexo(),
       c.getFuncao()
       });
       
       
       }
    
    }
